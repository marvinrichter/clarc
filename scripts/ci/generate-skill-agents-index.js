#!/usr/bin/env node
/**
 * Generate skills/SKILL_AGENTS.md — reverse index of agent→skill dependencies.
 *
 * For each skill, lists every agent that declares it in uses_skills frontmatter.
 * Groups skills by skill_family (read from skills/<name>/SKILL.md frontmatter).
 * Output is sorted: by family, then by skill name, then by agent name.
 *
 * Usage:
 *   node scripts/ci/generate-skill-agents-index.js
 *   node scripts/ci/generate-skill-agents-index.js --dry-run   # print without writing
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const AGENTS_DIR = path.join(__dirname, '../../agents');
const SKILLS_DIR = path.join(__dirname, '../../skills');
const OUTPUT_FILE = path.join(SKILLS_DIR, 'SKILL_AGENTS.md');

const isDryRun = process.argv.includes('--dry-run');

// ─── Frontmatter parser ──────────────────────────────────────────────────────

function parseFrontmatter(content) {
  const cleanContent = content.replace(/^\uFEFF/, '');
  const match = cleanContent.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) return {};

  const result = {};
  const lines = match[1].split(/\r?\n/);
  let currentKey = null;
  let currentArray = null;

  for (const line of lines) {
    if (/^\s{2}-\s+/.test(line) && currentKey) {
      const value = line.replace(/^\s{2}-\s+/, '').trim();
      if (!currentArray) {
        currentArray = [];
        result[currentKey] = currentArray;
      }
      currentArray.push(value);
      continue;
    }

    const colonIdx = line.indexOf(':');
    if (colonIdx > 0) {
      currentArray = null;
      currentKey = line.slice(0, colonIdx).trim();
      const value = line.slice(colonIdx + 1).trim();
      result[currentKey] = value || null;
    }
  }

  return result;
}

// ─── Step 1: Collect agent→skills mapping ────────────────────────────────────

/** @type {Map<string, string[]>} skill → agent names */
const skillToAgents = new Map();

const agentFiles = fs.existsSync(AGENTS_DIR)
  ? fs.readdirSync(AGENTS_DIR).filter(f => f.endsWith('.md'))
  : [];

for (const file of agentFiles) {
  const content = fs.readFileSync(path.join(AGENTS_DIR, file), 'utf-8');
  const fm = parseFrontmatter(content);

  const agentName = (fm.name || file.replace('.md', '')).replace(/['"]/g, '').trim();
  const usesSkills = fm.uses_skills;
  if (!usesSkills) continue;

  const skills = Array.isArray(usesSkills) ? usesSkills : [usesSkills];
  for (const skill of skills) {
    if (!skill || typeof skill !== 'string') continue;
    if (!skillToAgents.has(skill)) skillToAgents.set(skill, []);
    skillToAgents.get(skill).push(agentName);
  }
}

// ─── Step 2: Read skill families ─────────────────────────────────────────────

/** @type {Map<string, string>} skill → family */
const skillFamily = new Map();
/** @type {Map<string, string>} skill → description */
const skillDesc = new Map();

const allSkillDirs = fs.existsSync(SKILLS_DIR)
  ? fs.readdirSync(SKILLS_DIR).filter(d => {
      const skillFile = path.join(SKILLS_DIR, d, 'SKILL.md');
      return fs.existsSync(skillFile);
    })
  : [];

for (const dir of allSkillDirs) {
  const skillFile = path.join(SKILLS_DIR, dir, 'SKILL.md');
  const content = fs.readFileSync(skillFile, 'utf-8');
  const fm = parseFrontmatter(content);
  skillFamily.set(dir, fm.skill_family || 'other');
  skillDesc.set(dir, fm.description || '');
}

// ─── Step 3: Group by family ──────────────────────────────────────────────────

/** @type {Map<string, string[]>} family → skill names with at least one agent */
const familyToSkills = new Map();
const FAMILY_ORDER = ['testing', 'architecture', 'security', 'devops', 'ai-patterns', 'review', 'other'];

for (const [skill] of skillToAgents.entries()) {
  const family = skillFamily.get(skill) || 'other';
  if (!familyToSkills.has(family)) familyToSkills.set(family, []);
  familyToSkills.get(family).push(skill);
}

// Sort skills within each family
for (const skills of familyToSkills.values()) {
  skills.sort();
}

// Sort agents within each skill
for (const agents of skillToAgents.values()) {
  agents.sort();
}

// ─── Step 4: Generate markdown ───────────────────────────────────────────────

const totalSkills = skillToAgents.size;
const totalRefs = [...skillToAgents.values()].reduce((sum, a) => sum + a.length, 0);
const generatedAt = new Date().toISOString().slice(0, 10);

const lines = [
  `# Skill → Agent Reverse Index`,
  ``,
  `> Auto-generated by \`scripts/ci/generate-skill-agents-index.js\`.`,
  `> Last updated: ${generatedAt}`,
  `> ${totalSkills} skills referenced by ${totalRefs} agent→skill links across ${agentFiles.length} agents.`,
  ``,
  `Use \`/skill-impact <skill-name>\` to query this index interactively.`,
  ``,
  `---`,
  ``,
];

// Emit families in order, then any unlisted families alphabetically
const sortedFamilies = [
  ...FAMILY_ORDER.filter(f => familyToSkills.has(f)),
  ...[...familyToSkills.keys()].filter(f => !FAMILY_ORDER.includes(f)).sort(),
];

for (const family of sortedFamilies) {
  const skills = familyToSkills.get(family);
  if (!skills || skills.length === 0) continue;

  lines.push(`## ${capitalize(family)}`);
  lines.push(``);

  for (const skill of skills) {
    const agents = skillToAgents.get(skill) || [];
    const desc = skillDesc.get(skill) || '';
    lines.push(`### \`${skill}\``);
    lines.push(``);
    if (desc) { lines.push(desc); lines.push(``); }
    lines.push(`**Used by:** ${agents.map(a => `\`${a}\``).join(', ')}`);
    lines.push(``);
  }
}

// Skills with no agent refs (informational — skills no agent depends on)
const unreferencedSkills = allSkillDirs
  .filter(d => !skillToAgents.has(d))
  .sort();

if (unreferencedSkills.length > 0) {
  lines.push(`---`);
  lines.push(``);
  lines.push(`## Unreferenced Skills`);
  lines.push(``);
  lines.push(`These skills exist but are not declared in any agent's \`uses_skills\`. Consider adding references or checking if they are obsolete.`);
  lines.push(``);
  for (const skill of unreferencedSkills) {
    lines.push(`- \`${skill}\``);
  }
  lines.push(``);
}

const output = lines.join('\n');

// ─── Step 5: Write or dry-run ─────────────────────────────────────────────────

if (isDryRun) {
  console.log(output);
} else {
  fs.writeFileSync(OUTPUT_FILE, output, 'utf-8');
  console.log(`Generated skills/SKILL_AGENTS.md — ${totalSkills} skills, ${totalRefs} agent→skill links`);
}

// ─── Helpers ─────────────────────────────────────────────────────────────────

function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}
